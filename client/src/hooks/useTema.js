export function useTema() {
  const [temaActual, setTemaActual] = useState(
    () => localStorage.getItem('tema') || 'oceano'
  );

  const tema = TEMAS[temaActual];

  useEffect(() => {
    const root = document.documentElement;
    const props = {
      '--color-bg': tema.bg,
      '--color-bg-solid': tema.bgSolid,
      '--color-card': tema.card,
      '--color-card-solid': tema.cardSolid,
      '--color-primary': tema.primary,
      '--color-primary-solid': tema.primarySolid,
      '--color-primary-hover': tema.primaryHover,
      '--color-glass': tema.glass,
      '--color-glass-border': tema.glassBorder,
      '--color-text': tema.text,
      '--color-text-soft': tema.textSoft,
      '--color-text-muted': tema.textMuted,
      '--color-on-primary': tema.onPrimary,
      '--color-success': tema.success,
      '--color-danger': tema.danger,
      '--color-warning': tema.warning,
      '--color-border': tema.border,
      '--color-shadow': tema.shadow,
      '--color-shadow-hover': tema.shadowHover,
      '--color-accent': tema.accent,
      '--color-accent-soft': tema.accentSoft,
      '--color-text-secondary': tema.textMuted,
      '--bg-main': tema.bg,
      '--bg-card': tema.card,
      '--bg-sidebar': tema.cardSolid,
      '--bg-input': tema.cardSolid === '#18181B' ? '#27272A' : '#F9FAFB',
      '--text-primary': tema.text,
      '--text-secondary': tema.textMuted,
      '--text-muted': tema.textMuted,
      '--text-inverse': tema.onPrimary,
      '--border-color': tema.border,
      '--border-light': tema.border,
      '--success-bg': tema.success === '#00C896' ? '#E8FFF4' : 'rgba(74,222,128,0.15)',
      '--success-text': tema.success === '#00C896' ? '#009E78' : '#4ADE80',
      '--success-border': tema.success === '#00C896' ? '#B3FFE0' : 'rgba(74,222,128,0.3)',
      '--danger-bg': tema.danger === '#FF4757' ? '#FFE8EB' : 'rgba(248,113,113,0.15)',
      '--danger-text': tema.danger === '#FF4757' ? '#CC293B' : '#F87171',
      '--danger-border': tema.danger === '#FF4757' ? '#FFB3BE' : 'rgba(248,113,113,0.3)',
      '--warning-bg': tema.warning === '#FFB830' ? '#FFF8E6' : 'rgba(251,191,36,0.15)',
      '--warning-text': tema.warning === '#FFB830' ? '#CC8F00' : '#FBBF24',
      '--warning-border': tema.warning === '#FFB830' ? '#FFEBB3' : 'rgba(251,191,36,0.3)',
    };
    Object.entries(props).forEach(([key, val]) => root.style.setProperty(key, val));
    localStorage.setItem('tema', temaActual);
  }, [temaActual, tema]);

  return { temaActual, setTemaActual, tema, TEMAS };
}
